
public class MathExamples {

	public static void main(String[] args) {
		double pi_value = Math.PI;
		System.out.println("The value of pi is " + pi_value);
		
		double round_pi = Math.round(Math.PI);
		double ceil_pi = Math.ceil(Math.PI);
		
		System.out.println(round_pi + "," + ceil_pi);
		
		System.out.println(Math.sqrt(pi_value));
		
		int pi_val = (int) Math.PI; 
		System.out.println(pi_val);
		
		
		int numerator = 6;
		
		int denominator = 5;
		
		System.out.println(numerator / denominator);
		
		float result = (float) numerator/denominator;
		
		System.out.println(result);
				
		
		
		
	
	
		
		

	}

}
